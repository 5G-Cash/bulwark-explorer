
import TX from '../../model/tx';


const getTXLatest = (req, res) => {
  TX.find()
    .skip(req.query.skip ? parseInt(req.query.skip, 10) : 0)
    .limit(req.query.limit ? parseInt(req.query.limit, 10) : 50)
    .sort({ height: -1 })
    .then((docs) => {
      res.json(docs);
    })
    .catch((err) => {
      console.log(err);
      res.status(500).send(err.message || err);
    });
};

export default {
  getTXLatest
};
